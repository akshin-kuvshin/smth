SELECT
    Фамилия,
    Имя,
    Отчество
FROM
   (ВОДИТЕЛЬ AS ВД
    INNER JOIN СУБЪЕКТ_ПРАВОНАРУШЕНИЯ AS СП ON ВД.СНИЛС = СП.СНИЛС)
    INNER JOIN НАРУШЕНИЕ AS Н ON СП.Код_нарушения = Н.Код_нарушения
WHERE Пол = 'женский'
  AND Закон_кодекс = 'ПДД РФ'
  AND Нарушенный_пункт_статья = 'п. 6.2'
GROUP BY
    ВД.СНИЛС,
    Фамилия,
    Имя,
    Отчество
ORDER BY 1, 2, 3;

